if [ "$INSTALL_RULES" = true ]; then
echo -e "  ${BLUE}‚ÄĘ${NC} x402 Security Rules          ‚Üí ${CYAN}$TARGET_RULES_DIR${NC}"
fi
if [ "$INSTALL_COMMANDS" = true ]; then
echo -e "  ${BLUE}‚ÄĘ${NC} Commands                    ‚Üí ${CYAN}$TARGET_COMMANDS_DIR${NC}"
